    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a report row first.");
        return;
    }

    String reportId = jTable1.getValueAt(selectedRow, 0).toString(); // ID column
    String currentStatus = jTable1.getValueAt(selectedRow, 4).toString(); // Status column

    if (!currentStatus.equalsIgnoreCase("Pending....")) {
        JOptionPane.showMessageDialog(this, "Only pending reports can be assigned.");
        return;
    }

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DRMSRegister", "root", "");
        String updateQuery = "UPDATE reporteddisasters SET Status = 'Assigned' WHERE ID = ?";
        PreparedStatement pst = con.prepareStatement(updateQuery);
        pst.setString(1, reportId);

        int rowsAffected = pst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Responder Assigned Successfully.");
            loadDisasterReports(); // Reload table data
        } else {
            JOptionPane.showMessageDialog(this, "Failed to assign responder.");
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

