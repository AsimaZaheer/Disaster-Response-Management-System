    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String fullName = jTextField1.getText();
        String email = jTextField2.getText();
        String phone = jTextField3.getText();
        String username = jTextField4.getText();
        String password = String.valueOf(jPasswordField1.getPassword());
        String confirmPassword = String.valueOf(jPasswordField2.getPassword());
        String region = jTextField7.getText();
        String code = jTextField8.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/DRMSRegister", "root", "");

            String query = "SELECT * FROM RegisterAdmin WHERE FullName=? AND EmailAdress=? AND PhoneNumber=? AND UserName=? AND Password=? AND ConfirmPassword=? AND RegionSuppervised=? AND AdminCode=?";

            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, fullName);
            pst.setString(2, email);
            pst.setDouble(3, Double.parseDouble(phone));
            pst.setString(4, username);
            pst.setString(5, password);
            pst.setString(6, confirmPassword);
            pst.setString(7, region);
            pst.setDouble(8, Double.parseDouble(code));

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "✅ Admin Verified!");
                new Login().setVisible(true);
                this.dispose();
                new AdminProfile(username ).setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "❌ Invalid Admin Information!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            con.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }

    // Dummy AdminProfile class

    }//GEN-LAST:event_jButton1MouseClicked
